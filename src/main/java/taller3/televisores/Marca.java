package taller3.televisores;

public class Marca {

	private String nombre;
	
	//constructor
	public Marca(String nombre) {
		this.nombre = nombre;
		
	}
	//metodo set
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	//metodo get
	public String getNombre(){
		
		return nombre;
		
	}

}
